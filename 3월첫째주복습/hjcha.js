function solution(m, n, puddles) {
    // 1. DP 테이블 초기화
    // 계산 편의를 위해 0행과 0열을 포함한 (n+1) x (m+1) 크기로 생성.
    // 길을 찾지 못한 곳은 0으로 시작해야 경로 합산 시 영향이 없음.
    const dp = Array.from({ length: n + 1 }, () => Array(m + 1).fill(0));

    // 2. 장애물(웅덩이) 사전 처리
    // 문제의 좌표가 [열, 행] 순서이므로 dp[행][열]에 맞춰 위치 표시.
    // 웅덩이를 -1로 두어 일반적인 '경로 0'인 지점과 구분함.
    for (const [col, row] of puddles) {
        dp[row][col] = -1;
    }

    // 3. 베이스 케이스(시작점) 설정
    // 집의 위치(1, 1)에서 시작하는 경우의 수는 1가지.
    dp[1][1] = 1;

    // 4. 효율적인 경로 찾기 (이중 루프)
    for (let i = 1; i <= n; i++) {
        for (let j = 1; j <= m; j++) {
            // [Check] 시작 지점은 이미 설정했으므로 스킵
            if (i === 1 && j === 1) continue;

            // [Check] 현재 칸이 웅덩이라면?
            // 더 이상 경로를 확장할 수 없으므로 0으로 초기화하고 다음 칸으로.
            if (dp[i][j] === -1) {
                dp[i][j] = 0;
                continue;
            }

            // [핵심 점화식] 최단 거리는 '오른쪽'과 '아래'로만 이동 가능함.
            // 역으로 생각하면 현재 칸은 '위쪽'이나 '왼쪽'에서만 올 수 있음.
            // 효율성 테스트 통과를 위해 매 단계에서 1,000,000,007로 나누어 오버플로우 방지.
            dp[i][j] = (dp[i - 1][j] + dp[i][j - 1]) % 1000000007;
        }
    }

    // 학교가 있는 최종 목적지의 경로 합산 결과 반환
    return dp[n][m];
}